"""We have v=u+at where v is velocity, u is initial velocity, and t is the time.
And value for each is as follows: v=25m/s. u=0m/s, t=10 seconds. Find the
acceleration a. [Rearrange the equation, also use proper formatting syntax
while displaying the answer.]"""

v = 25
u = 0
t = 10
a = (v - u)/t
print(f"\nAcceleration: {a}\n")
